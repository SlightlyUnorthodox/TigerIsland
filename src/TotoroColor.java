public enum TotoroColor {

    BLUE(0, "Blue"),
    BROWN(1, "Brown");

    private int code;
    private String color;


    TotoroColor(int code, String color) {
        this.code = code;
        this.color = color;
    }

    public int getCode() {
        return code = code;
    }

    public String getColor() {
        return color;
    }

}
